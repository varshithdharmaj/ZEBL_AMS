/**
 * After `prisma generate`, ensure browser-safe enum imports have full types.
 *
 * Prisma emits `enums.ts` as `export * from "./index-browser"` but does not
 * emit `index-browser.d.ts` next to `index-browser.js`. Relative imports then
 * see values-only (allowJs), which causes ~TS2749 and string→enum errors.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const target = path.join(root, "src/generated/prisma/index-browser.d.ts");

const contents = `/**
 * Type declarations for the browser-safe Prisma entry.
 *
 * \`enums.ts\` re-exports from \`./index-browser\`. Without this file, TypeScript
 * resolves only the .js runtime (allowJs) and treats enum members as \`string\`,
 * and enum identifiers as values-only (TS2749 when used as types).
 *
 * Mirrors package.json exports: "./index-browser" → "types": "./index.d.ts"
 * for relative imports that bypass the package exports map.
 *
 * Regenerated by scripts/patch-prisma-browser-types.mjs after prisma generate.
 */
export * from "./index";
`;

fs.mkdirSync(path.dirname(target), { recursive: true });
fs.writeFileSync(target, contents, "utf8");
console.log(`[patch-prisma-browser-types] wrote ${path.relative(root, target)}`);
